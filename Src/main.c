void homeScreen()
{
	Home = 1;
	Settings = 0;
	Version = 0;
	MenuUp = 0;
	MenuDown = 0;

	ST7789_SetRotation(3);

	ST7789_WriteString(30, 45, "HOME MENU", Font_11x18, BLACK, WHITE);

		  }


void settingScreen()
{
	Home = 0;
	Settings = 1;
	Version = 0;

	 ST7789_WriteString(30, 45, "SETTING MENU", Font_11x18, BLACK, WHITE);

}


void versionScreen()
{
	Home = 0;
	Settings = 0;
	Version = 1;

	  ST7789_WriteString(30, 45, "VERSION MENU", Font_11x18, BLACK, WHITE);

}


// Main Menu program loop with 3 screens (Home, Settings & Version)
void loop()
{

	if(ButtonSelect1==1)
	{
		ST7789_Fill_Color(WHITE);
		ButtonSelect1=0;
	}
	else if(ButtonSelect2==1)
	{
			ST7789_Fill_Color(WHITE);
			ButtonSelect2=0;
	}

	if(Home == 1 && MenuUp == 0 && MenuDown == 0)
	{
		MenuToggle = 1;
	}
	else if(Home == 1 && MenuUp == 1 && MenuDown == 0)
	{
		MenuToggle = 2;
	}
	else if(Home == 1 &&  MenuDown == 1 && MenuUp == 0 )
	{
		MenuToggle = 0;
	}

	if(Settings == 1 && MenuUp == 0 && MenuDown == 0)
	{
		MenuToggle = 2;
	}
	else if(Settings == 1 && MenuUp == 1 && MenuDown == 0)
	{
		MenuToggle = 2;
	}
	else if(Settings == 1 &&  MenuDown == 1 && MenuUp == 0 )
	{
		MenuToggle = 1;
	}

	if(Version == 1 && MenuUp == 0 && MenuDown == 0)
		{
		MenuToggle = 0;
		}
	else if(Version == 1 && MenuUp == 1 && MenuDown == 0 )
		{
		MenuToggle = 1;
		}
	else if(Version == 1 && MenuDown == 1 && MenuUp == 0 )
		{
		MenuToggle = 0;
		}

 switch(MenuToggle)
		{
	case 0:
	versionScreen();
	HAL_Delay(500);
    break;

	case 1:
	homeScreen();
	HAL_Delay(500);
	break;

	case 2:
	settingScreen();
	HAL_Delay(500);
	break;

	default:
    homeScreen();
	HAL_Delay(500);
	break;
	}
}

